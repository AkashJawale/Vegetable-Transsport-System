    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        con=ConnectDB.condb();
        try
        {
            pst=(OraclePreparedStatement)con.prepareStatement("UPDATE TRANSPORT SET V_NO=?,V_TYPE=?,DRI_ID=?,DRI_NAME=?,DRI_CONT=?,PRO_1=?,PRO_2=?,PRO_3=?,R_FROM=?,R_TO=?,TOT_DIS=?,D_NAME=?,D_CONT where V_NO=" +txtVid.getText());
           
             pst.setInt(1, Integer.parseInt(txtVid.getText()+""));
             pst.setString(2,txtVno.getText()+"");
             pst.setString(3,txtVtype.getText()+"");
            pst.setInt(4, Integer.parseInt(txtDriid.getText()+""));
            pst.setString(5,txtDriname.getText()+"");
            pst.setString(6,txtDricont.getText()+"");
            pst.setString(7,cmbPro1.getSelectedItem()+"");
            pst.setString(8,cmbPro2.getSelectedItem()+"");
            pst.setString(9,cmbPro3.getSelectedItem()+"");
            pst.setString(10,txtRfrom.getText()+"");
            pst.setString(11,txtRto.getText()+"");
            pst.setString(12,txtTotdis.getText()+"");
            pst.setString(13,txtDname.getText()+"");
            pst.setString(14,txtDcont.getText()+"");
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Updated...");
            pst.close();
            
        }
        catch(Exception e)
        {
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

