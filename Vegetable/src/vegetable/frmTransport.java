    private void txtVidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtVidKeyPressed
        // TODO add your handling code here:
        
        int key=evt.getKeyCode();
         if ((key>=evt.VK_0&&key<=evt.VK_9)||(key>=evt.VK_NUMPAD0&&key<=evt.VK_NUMPAD9)||key==KeyEvent.VK_BACK_SPACE)
         {
            txtVid.setEditable(true);
            txtVid.setBackground(Color.white);
             
            
         }
         else
         {    
            txtVid.setEditable(false);
        txtVid.setBackground(Color.red);
         
        }
        
        
        
    }//GEN-LAST:event_txtVidKeyPressed

